// Operadores logicos
bool x = true;
bool y = false;

bool and = x && y; // e logico
bool or = || y; // ou logico
bool not = !x; // nao logico

// Exibindo resultado dos operadores logicos
Console.WriteLine("\noperadores logicos:");
Console.WriteLine($"x && y: {and}");  //
Console.WriteLine($"x || y: {or}");  //
Console.WriteLine($"!x: {not}");   //